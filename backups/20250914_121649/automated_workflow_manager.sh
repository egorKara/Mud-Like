    echo -e "${GREEN}‚úÖ –ï–ñ–ï–ù–ï–î–ï–õ–¨–ù–´–ô –ü–†–û–¶–ï–°–° –ó–ê–í–ï–†–®–ï–ù${NC}"
    log_workflow "WORKFLOW: –ï–∂–µ–Ω–µ–¥–µ–ª—å–Ω—ã–π –ø—Ä–æ—Ü–µ—Å—Å –∑–∞–≤–µ—Ä—à–µ–Ω —É—Å–ø–µ—à–Ω–æ"
}

# –§—É–Ω–∫—Ü–∏—è –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ –µ–∂–µ–Ω–µ–¥–µ–ª—å–Ω–æ–≥–æ –æ—Ç—á–µ—Ç–∞
generate_weekly_report() {
    local report_file="WEEKLY_REPORT_$(date +%Y%m%d).md"
    
    echo "# üìä –ï–ñ–ï–ù–ï–î–ï–õ–¨–ù–´–ô –û–¢–ß–ï–¢ –ü–†–û–ï–ö–¢–ê MUD-LIKE" > "$report_file"
    echo "**–î–∞—Ç–∞:** $(date '+%d.%m.%Y %H:%M:%S')" >> "$report_file"
    echo "" >> "$report_file"
    
    echo "## üéØ –¶–ï–õ–¨ –ü–†–û–ï–ö–¢–ê" >> "$report_file"
    echo "–°–æ–∑–¥–∞–Ω–∏–µ MudRunner-like –∏–≥—Ä—ã —Å —Ä–µ–∞–ª–∏—Å—Ç–∏—á–Ω–æ–π —Ñ–∏–∑–∏–∫–æ–π –≤–Ω–µ–¥–æ—Ä–æ–∂–Ω–∏–∫–∞ –∏ –¥–µ—Ñ–æ—Ä–º–∞—Ü–∏–µ–π —Ç–µ—Ä—Ä–µ–π–Ω–∞" >> "$report_file"
    echo "" >> "$report_file"
    
    echo "## üìä –°–¢–ê–¢–ò–°–¢–ò–ö–ê –ü–†–û–ï–ö–¢–ê" >> "$report_file"
    local cs_files=$(find Assets -name "*.cs" | wc -l | tr -d ' ')
    local total_lines=$(find Assets -name "*.cs" -exec wc -l {} + 2>/dev/null | tail -1 | awk '{print $1}' || echo "0")
    local ecs_components=$(find Assets -name "*.cs" -exec grep -l "IComponentData\|IBufferElementData" {} \; 2>/dev/null | wc -l | tr -d ' ')
    local ecs_systems=$(find Assets -name "*.cs" -exec grep -l "SystemBase\|ISystem" {} \; 2>/dev/null | wc -l | tr -d ' ')
    
    echo "- üìÅ –§–∞–π–ª–æ–≤ C#: $cs_files" >> "$report_file"
    echo "- üìù –°—Ç—Ä–æ–∫ –∫–æ–¥–∞: $total_lines" >> "$report_file"
    echo "- üéØ ECS –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–æ–≤: $ecs_components" >> "$report_file"
    echo "- üöÄ ECS —Å–∏—Å—Ç–µ–º: $ecs_systems" >> "$report_file"
    echo "" >> "$report_file"
    
    echo "## üîß –ò–ù–°–¢–†–£–ú–ï–ù–¢–´ –ê–í–¢–û–ú–ê–¢–ò–ó–ê–¶–ò–ò" >> "$report_file"
    echo "–í—Å–µ –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç—ã –∞–≤—Ç–æ–º–∞—Ç–∏–∑–∞—Ü–∏–∏ —Ä–∞–±–æ—Ç–∞—é—Ç –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ:" >> "$report_file"
    echo "- ‚úÖ optimized_unity_monitor.sh" >> "$report_file"
    echo "- ‚úÖ asset_import_worker_fixer.sh" >> "$report_file"
    echo "- ‚úÖ immutable_packages_fixer.sh" >> "$report_file"
    echo "- ‚úÖ preventive_unity_maintenance.sh" >> "$report_file"
    echo "- ‚úÖ ultimate_unity_optimizer.sh" >> "$report_file"
    echo "- ‚úÖ continuous_unity_guardian.sh" >> "$report_file"
    echo "" >> "$report_file"
    
    echo "## üéØ –°–¢–ê–¢–£–° –ü–†–û–ï–ö–¢–ê" >> "$report_file"
    echo "–ü—Ä–æ–µ–∫—Ç –≤ –æ—Ç–ª–∏—á–Ω–æ–º —Å–æ—Å—Ç–æ—è–Ω–∏–∏ –∏ –≥–æ—Ç–æ–≤ –∫ —Ä–∞–∑—Ä–∞–±–æ—Ç–∫–µ MudRunner-like –∏–≥—Ä—ã." >> "$report_file"
    echo "" >> "$report_file"
    
    echo "  ‚úÖ –û—Ç—á–µ—Ç —Å–æ–∑–¥–∞–Ω: $report_file"
    log_workflow "REPORT: –ï–∂–µ–Ω–µ–¥–µ–ª—å–Ω—ã–π –æ—Ç—á–µ—Ç —Å–æ–∑–¥–∞–Ω - $report_file"
}

# –§—É–Ω–∫—Ü–∏—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–≥–æ –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–∏—è –ø—Ä–æ–±–ª–µ–º
auto_fix_workflow() {
