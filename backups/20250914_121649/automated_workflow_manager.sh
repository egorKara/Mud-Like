    echo "  ‚úÖ –ü—Ä–æ–µ–∫—Ç –æ–ø—Ç–∏–º–∏–∑–∏—Ä–æ–≤–∞–Ω"
    
    echo ""
    echo -e "${GREEN}‚úÖ –ï–ñ–ï–î–ù–ï–í–ù–´–ô –ü–†–û–¶–ï–°–° –ó–ê–í–ï–†–®–ï–ù${NC}"
    log_workflow "WORKFLOW: –ï–∂–µ–¥–Ω–µ–≤–Ω—ã–π –ø—Ä–æ—Ü–µ—Å—Å –∑–∞–≤–µ—Ä—à–µ–Ω —É—Å–ø–µ—à–Ω–æ"
